var (valor1, valor2, valor3) = (10, 10, 20);
Console.WriteLine("Los valores son: " + valor1 + ", " + valor2 + ", " + valor3);

//Operadores relacionales
/*
== Igual a
!= Diferente de 
> Mayor que
< Menor que
>= Mayor o igual que
<= Menor o igual que
*/

bool resultado1 = valor1 == valor2;
Console.WriteLine("La comparacion de valor1 == valor2 es: " + resultado1);

bool resultado2 = valor1 != valor2;
Console.WriteLine("La comparacion de valor1 != valor2 es: " + resultado2);

bool resultado3 = valor1 > valor2;
Console.WriteLine("La comparacion de valor1 > valor2 es: " + resultado3);

bool resultado4 = valor1 < valor2;
Console.WriteLine("La comparacion de valor1 < valor2 es: " + resultado4);

bool resultado5 = valor1 >= valor2;
Console.WriteLine("La comparacion de valor1 >= valor2 es: " + resultado5);

bool resultado6 = valor1 <= valor2;
Console.WriteLine("La comparacion de valor1 <= valor2 es: " + resultado6);